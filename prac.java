public class prac {
 public static void main(String[] args) {
     //two types of casting 
    //1. Widening 
    //2. Narrowing
    
    //1.
    // smaller data type into large data type
    int a = 6;
    double name = a;
    System.out.println(name);

    //2.
    //Larger into smaller
    float name1 = 65.29f;
    int a1 = (int) name1;
    System.out.println(a1);
 }   
}
